package sshr.eventmodel;


public class EventAccessNotAllowedException extends java.lang.Exception {

	public EventAccessNotAllowedException() {

		super();
	}

	public EventAccessNotAllowedException(String message) {

		super(message);
	}

	public EventAccessNotAllowedException(Throwable cause) {

		super(cause);
	}

	public EventAccessNotAllowedException(String message, Throwable cause) {

		super(message, cause);
	}

}
