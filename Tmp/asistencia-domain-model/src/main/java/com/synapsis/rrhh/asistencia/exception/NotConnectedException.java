
package com.synapsis.rrhh.asistencia.exception;

public class NotConnectedException extends Exception {

    public NotConnectedException() {}

    public NotConnectedException(String message) {super(message);}

    public NotConnectedException(Throwable ex) {super(ex);}

    public NotConnectedException(String message, Throwable ex) {super(message, ex);}

}
